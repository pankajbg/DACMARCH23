class pattern8_2{
	public static void main(String args[])
	{
		for(int i=1; i<=5;i++)
		{
			for(int j=1;j<=i;j++)
			{
				System.out.print("*");
			}
		 System.out.println();
		}
		for(int i1=4; i1>=1; i1--)
		 {
			for(int j1=1;j1<=i1;j1++)
			{
				System.out.print("*");
			}
		 System.out.println();
		 }
	}
}
		
		
		
		
			